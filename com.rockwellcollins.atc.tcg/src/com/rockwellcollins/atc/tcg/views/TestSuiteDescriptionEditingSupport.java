package com.rockwellcollins.atc.tcg.views;

import org.eclipse.jface.viewers.CellEditor;
import org.eclipse.jface.viewers.EditingSupport;
import org.eclipse.jface.viewers.TableViewer;
import org.eclipse.jface.viewers.TextCellEditor;
import com.rockwellcollins.atc.tcg.suite.TestCase;

public class TestSuiteDescriptionEditingSupport extends EditingSupport {

	private final TableViewer viewer;
	private final TextCellEditor editor;

	public TestSuiteDescriptionEditingSupport(TableViewer viewer) {
		super(viewer);
		this.viewer = viewer;
		this.editor = new TextCellEditor(viewer.getTable());
	}

	@Override
	protected CellEditor getCellEditor(Object element) {
		return editor;
	}

	@Override
	protected boolean canEdit(Object element) {
		return true;
	}

	@Override
	protected Object getValue(Object element) {
		if (element instanceof TestCase) {
			TestCase tc = (TestCase)element;
			String desc = tc.getDescription();
			return (Object)desc;
		}
		return null;
	}

	@Override
	protected void setValue(Object element, Object value) {
		if (element instanceof TestCase) {
			TestCase tc = (TestCase)element;
			tc.setDescription((String)value);
			viewer.update(element, null);
		}
	}

}
